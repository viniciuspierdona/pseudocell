    private void jButtonExportNodesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExportNodesActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "This action will save any changes made to the nodes. Would you like to proceed?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogType(javax.swing.JFileChooser.SAVE_DIALOG);
            int returnVal = fileChooser.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = fileChooser.getSelectedFile();
                try {
                    // Export node settings
                    XMLNetworkWriter.exportNodeList(file.getAbsolutePath(), nodeList);
                    // Concurrently saves node settings to temporary file
                    XMLNetworkWriter.exportNodeList(configurations.networkSourceFileName, nodeList);
                    // Ensures that the system takes into account user-placed node configurations
                    configurations.networkSourceInputType = "xml";
                    JOptionPane.showMessageDialog(this, "Export completed successfully.", "Successful Import", JOptionPane.INFORMATION_MESSAGE);
                } catch (ParserConfigurationException | IOException | SAXException | TransformerException ex) {
                    java.util.logging.Logger.getLogger(NodeConfigurations.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
            } else {
                System.out.println("File access cancelled by user.");
            }
        }
    }//GEN-LAST:event_jButtonExportNodesActionPerformed

    private void jButtonImportNodesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImportNodesActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "This action will replace the current network, including its rules, with the chosen network. Would you like to proceed?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogType(javax.swing.JFileChooser.OPEN_DIALOG);
            int returnVal = fileChooser.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                try {
                    File file = fileChooser.getSelectedFile();
                    // Replaces the list of current nodes with those submitted by the user.
                    nodeList = XMLNetworkReader.importNodeList(file);
                    // Simultaneously replaces the nodes' local file with those submitted by the user.
                    XMLNetworkWriter.exportNodeList(configurations.networkSourceFileName, nodeList);
                    configurations.networkSourceInputType = "xml";
                    setComponents();
                    JOptionPane.showMessageDialog(this, "File imported successfully.", "Successful Import", JOptionPane.INFORMATION_MESSAGE);
                } catch (ParserConfigurationException | IOException | SAXException | TransformerException ex) {
                    java.util.logging.Logger.getLogger(NodeConfigurations.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
            } else {
                System.out.println("File access cancelled by user.");
            }
        }
    }//GEN-LAST:event_jButtonImportNodesActionPerformed

    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "This action will save any changes made to the nodes. Would you like to proceed?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            // Checks if a node has been selected by the user.
            if (jTextFieldName.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "[ERROR] No nodes were selected.", "Error - Empty Node", JOptionPane.ERROR_MESSAGE);
                return;
            }
            selectedNode.setStatus(Double.valueOf(jTextFieldStatus.getText()));
            selectedNode.setRestingStatus(Double.valueOf(jTextFieldRestingStatus.getText()));
            selectedNode.setStatus(Double.valueOf(jTextFieldStatus.getText()));
            if (selectedNode.getType() == 2) {
                ((Metabolite) selectedNode).setIncome(Double.valueOf(jTextFieldIncome.getText()));
                ((Metabolite) selectedNode).setMinValue(Double.valueOf(jTextFieldMinValue.getText()));
                ((Metabolite) selectedNode).setMaxValue(Double.valueOf(jTextFieldMaxValue.getText()));
            }
            // Ensures that the network used in the execution contains the changes made by the user.
            configurations.networkSourceInputType = "xml";
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jButtonEditModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditModActionPerformed
        // Checks if a node has been selected by the user.
        if (jTextFieldName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "[ERROR] No nodes were selected.", "Error - Empty Node", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Checks if the node has any modifications to be edited.
        if (jComboBoxModifications.getItemAt(0).equals("<none>")) {
            JOptionPane.showMessageDialog(this, "[ERROR] This node has no modifications available for editing.", "Error - Empty Node", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int modificationIndex = jComboBoxModifications.getSelectedIndex();
        EditModification frame = new EditModification(configurations, nodeList, selectedNode, modificationIndex);
        frame.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
        frame.setVisible(true);
    }//GEN-LAST:event_jButtonEditModActionPerformed

