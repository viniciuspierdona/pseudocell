    private void listNodesListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listNodesListMouseClicked
        // Ensures the item will only be accessed on the second click
        if (evt.getClickCount() == 2) {
            int selectedIndex = listNodesList.getSelectedIndex();
            selectedNode = displayedNodes.get(selectedIndex);
            boolean metabolite = false;
            //
            jTextFieldName.setText(selectedNode.getName());
            jTextFieldStatus.setText(String.valueOf(selectedNode.getStatus()));
            jTextFieldRestingStatus.setText(String.valueOf(selectedNode.getRestingStatus()));
            // Checks if the node has modifications and, if so, populates the ComboBox
            jComboBoxModifications.removeAllItems();
            if (!selectedNode.getNodeModifications().isEmpty()) {
                for (int i = 0; i < selectedNode.getNodeModifications().size(); i++) {
                    jComboBoxModifications.addItem(selectedNode.getNodeModifications().get(i).getName());
                }
            } else {
                jComboBoxModifications.addItem("<none>");
            }
            // Set node type
            switch (selectedNode.getType()) {
                case TYPE_PROTEINGENE:
                    jTextFieldType.setText("Protein/Gene");
                    break;
                case TYPE_METABOLITE:
                    jTextFieldType.setText("Metabolite");
                    break;
                case TYPE_OTHER:
                    jTextFieldType.setText("Other");
                    break;
                case TYPE_MIRNA:
                    jTextFieldType.setText("miRNA");
                    ;
                    break;
                case TYPE_DRUG:
                    jTextFieldType.setText("Drug");
                    break;
                default:
                    throw new AssertionError();
            }
            // If the node is a metabolite, populate specific fields for metabolites. If not, block these fields.
            if (selectedNode.getType() == 2) {
                jTextFieldIncome.setText(String.valueOf(((Metabolite) selectedNode).getIncome()));
                jTextFieldMinValue.setText(String.valueOf(((Metabolite) selectedNode).getMinValue()));
                jTextFieldMaxValue.setText(String.valueOf(((Metabolite) selectedNode).getMaxValue()));
                metabolite = true;
            }

            jTextFieldIncome.setEditable(metabolite);
            jTextFieldMinValue.setEditable(metabolite);
            jTextFieldMaxValue.setEditable(metabolite);
            jTextFieldRestingStatus.setEditable(!metabolite);
            setFieldColor(metabolite);
        }
    }//GEN-LAST:event_listNodesListMouseClicked

