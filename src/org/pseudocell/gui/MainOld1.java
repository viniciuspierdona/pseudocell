    private void listNCNodesListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listNCNodesListMouseClicked
        if (!itemSelected) { // Ensures the item will only be accessed on the second click
            itemSelected = true;
            return;
        }
        // Populates the node edit fields
        int selectedIndex = listNCNodesList.getSelectedIndex();
        Node selectedNode = nodesDisplayed.get(selectedIndex);
        jTextFieldNCName.setText(selectedNode.getName());
        jTextFieldNCRestingStatus.setText(String.valueOf(selectedNode.getRestingStatus()));
        loadModificationsComboBox(selectedNode);
        switch (selectedNode.getType()) {
            case 1:
                jComboBoxNCType.setSelectedIndex(0);
                break;
            case 2:
                jComboBoxNCType.setSelectedIndex(1);
                break;
            case 3:
                jComboBoxNCType.setSelectedIndex(4);
                break;
            case 4:
                jComboBoxNCType.setSelectedIndex(2);
                break;
            case 5:
                jComboBoxNCType.setSelectedIndex(3);
                break;
            default:
                throw new AssertionError();
        }
        if (selectedNode.getType() == 2) {
            jTextFieldNCIncome.setText(String.valueOf(((Metabolite) selectedNode).getIncome()));
            jTextFieldNCMinValue.setText(String.valueOf(((Metabolite) selectedNode).getMaxValue()));
            jTextFieldNCMaxValue.setText(String.valueOf(((Metabolite) selectedNode).getMinValue()));
            setMetabolicFields(true);
        } else {
            setMetabolicFields(false);
        }
        // Reset double click
        itemSelected = false;
    }//GEN-LAST:event_listNCNodesListMouseClicked

